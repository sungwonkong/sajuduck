export const results: FortuneResult[] = Array.from({ length: TOTAL_RESULT_COUNT }, (_, index) => {
  const id = index + 1;
  const gapja = `${heavenlyStems[index % heavenlyStems.length]}${earthlyBranches[index % earthlyBranches.length]}`;
  const titleCore = titleCores[index % titleCores.length];
  const suffix = titleSuffixes[Math.floor(index / titleCores.length) % titleSuffixes.length];
  const grades = gradePatterns[index % gradePatterns.length];
  const oneLiner = oneLiners[index];

  return {
    id,
    title: `${titleCore}${suffix}`,
    animal: gapja,
    element: elements[index % elements.length],
    characterMood: moods[index % moods.length],
    oneLiner,
    description: `${descriptionSeeds[index % descriptionSeeds.length]} ${oneLiner}라는 신호가 뜬 날이니, 너무 진지하게 붙잡기보다 가볍게 웃고 지나가면 운이 더 잘 붙습니다.`,
    ...grades,
    luckyItem: luckyItems[index % luckyItems.length],
    luckyColor: luckyColors[index % luckyColors.length],
    tags: [
      tagPool[index % tagPool.length],
      tagPool[(index + 11) % tagPool.length],
      `사주짤${String(id).padStart(3, "0")}`,
    ],
  };
});

export function getResultById(id: number): FortuneResult {
  const normalizedId = ((id - 1) % TOTAL_RESULT_COUNT) + 1;

  return results[normalizedId - 1];
}
